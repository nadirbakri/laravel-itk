<script type="text/javascript">
    function savePreviousURL() {
        if(!sessionStorage.getItem('previousURLTwo')){
            const previousURL = document.referrer;
            sessionStorage.setItem('previousURLTwo', previousURL);
        }
    }

    // Fungsi untuk menangani navigasi mundur
    function goBackWithModuleID() {
        let newURL = sessionStorage.getItem('previousURLTwo');

        sessionStorage.removeItem('previousURLTwo');

        window.location.href = newURL;
    }

    window.onload = function() {
        savePreviousURL();
    }

    var prevYear = moment(moment().subtract(10, 'years')).format('YYYY');
    var nextYear = moment(moment().add(10, 'years')).format('YYYY');
    var defaultYear = moment().format('YYYY');

    for (var i = prevYear; i <= nextYear; i++){
        var option = $("<option/>", {
            value: i,
            text: i
        });
        $('#year').append(option);
        $('#year').val(defaultYear);
    }
    
    $(document).ready(function () {
        var func = "{{ $func }}";
        var arrData = @json($data);
        var CSRF_TOKEN = $('meta[name="csrf-token"]').attr('content');

        $('#code').select2();

        if (func == 'new') {
            $('#record_function').val("New");
            $('#vaccine_code').val("");
            $('#vaccine_name').val("");
        } else if (func == 'edit') {
            $('#record_function').val("Edit");
            $('#vaccine_code').val(arrData[0].activityCode);
            $('#vaccine_name').val(arrData[0].activityName);
        }

        $('#notification_success').on('hide.bs.modal', function () {
            window.location = "{{ url('medical/vaccine_master') }}";
        })

        $("#btn-save").click(function () {
            $(this).prop("disabled", true);
            $(this).html(
                '<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span> Loading...'
            );
            $("#vaccine_master_form").submit();
        });

        if ($("#vaccine_master_form").length > 0) {
            $("#vaccine_master_form").validate({
                rules: {
                    vaccine_code: {
                        required: true,
                    },
                    vaccine_name: {
                        required: true,
                    }
                },
                messages: {
                    vaccine_code: {
                        required: "{{ __('md_vaccine_master.vaccine_code_required') }}",
                    },
                    vaccine_name: {
                        required: "{{ __('md_vaccine_master.vaccine_name_required') }}",
                    }
                },
                highlight: function (element) {
                    $(element).addClass('is-invalid');
                },
                unhighlight: function (element) {
                    $(element).removeClass('is-invalid');
                },
                errorElement: 'span',
                errorPlacement: function (error, element) {
                    $("#btn-save").prop("disabled", false);
                    $("#btn-save").html(
                        '<i class="fa fa-floppy-o"></i> {{ __("md_vaccine_master.btn_save") }}'
                    );
                    error.addClass('invalid-feedback');
                    element.closest('.form-group').append(error);
                },
                submitHandler: function (form) {
                    $.ajaxSetup({
                        headers: {
                            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                        }
                    });
                    $.ajax({
                        url: "{{ url('medical/vaccine_master/proses') }}",
                        type: "POST",
                        data: $('#vaccine_master_form').serialize(),
                        success: function (response) {
                            if (response.status == "true") {
                                $("#btn-save").prop("disabled", false);
                                $("#btn-save").html(
                                    '<i class="fa fa-floppy-o"></i> {{ __("md_vaccine_master.btn_save") }}'
                                );
                                $('#notification_success').modal('show');
                                $('#message-notification-success').html(response
                                    .message);
                                setTimeout(function () {
                                    window.location =
                                        "{{ url('medical/vaccine_master') }}";
                                }, 3000);
                            } else {
                                $("#btn-save").prop("disabled", false);
                                $("#btn-save").html(
                                    '<i class="fa fa-floppy-o"></i> {{ __("md_vaccine_master.btn_save") }}'
                                );
                                $('#notification_error').modal('show');
                                if (response.message == null || response.message ==
                                    '') {
                                    $('#message-notification-error').html(
                                        "{{ __('login.error') }}");
                                } else {
                                    $('#message-notification-error').html(response
                                        .message);
                                }
                            }
                        },
                        error: function (response) {
                            $("#btn-save").prop("disabled", false);
                            $("#btn-save").html(
                                '<i class="fa fa-floppy-o"></i> {{ __("md_vaccine_master.btn_save") }}'
                            );
                            $('#notification_error').modal('show');
                            $('#message-notification-error').html(response);
                        }
                    });
                }
            })
        }
    });

